package com.biscuit.b1.model;

public class StoreVO {

	private int store_num;
	private String store_name;
	private int store_price;
	private int store_sale;
	private String store_note;
	private int store_package;
	private String store_img;
	private String store_thumbimg;
	
	public int getStore_num() {
		return store_num;
	}
	public void setStore_num(int store_num) {
		this.store_num = store_num;
	}
	public String getStore_name() {
		return store_name;
	}
	public void setStore_name(String store_name) {
		this.store_name = store_name;
	}
	public int getStore_price() {
		return store_price;
	}
	public void setStore_price(int store_price) {
		this.store_price = store_price;
	}
	public int getStore_sale() {
		return store_sale;
	}
	public void setStore_sale(int store_sale) {
		this.store_sale = store_sale;
	}
	public String getStore_note() {
		return store_note;
	}
	public void setStore_note(String store_note) {
		this.store_note = store_note;
	}
	public int getStore_package() {
		return store_package;
	}
	public void setStore_package(int store_package) {
		this.store_package = store_package;
	}
	public String getStore_img() {
		return store_img;
	}
	public void setStore_img(String store_img) {
		this.store_img = store_img;
	}
	public String getStore_thumbimg() {
		return store_thumbimg;
	}
	public void setStore_thumbimg(String store_thumbimg) {
		this.store_thumbimg = store_thumbimg;
	}
}
