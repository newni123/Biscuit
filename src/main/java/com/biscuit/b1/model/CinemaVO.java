package com.biscuit.b1.model;

public class CinemaVO {
	
	private int cinema_num;
	private String cinema_loc;
	private String cinema_name;
	private String cinema_add;
	private String cinema_tel;
	private int cinema_locNum;
	
	private String cinema_image;
	
	
	
	public String getCinema_image() {
		return cinema_image;
	}
	public void setCinema_image(String cinema_image) {
		this.cinema_image = cinema_image;
	}
	public int getCinema_num() {
		return cinema_num;
	}
	public void setCinema_num(int cinema_num) {
		this.cinema_num = cinema_num;
	}
	public String getCinema_loc() {
		return cinema_loc;
	}
	public void setCinema_loc(String cinema_loc) {
		this.cinema_loc = cinema_loc;
	}
	public String getCinema_name() {
		return cinema_name;
	}
	public void setCinema_name(String cinema_name) {
		this.cinema_name = cinema_name;
	}
	public String getCinema_add() {
		return cinema_add;
	}
	public void setCinema_add(String cinema_add) {
		this.cinema_add = cinema_add;
	}
	public String getCinema_tel() {
		return cinema_tel;
	}
	public void setCinema_tel(String cinema_tel) {
		this.cinema_tel = cinema_tel;
	}
	public int getCinema_locNum() {
		return cinema_locNum;
	}
	public void setCinema_locNum(int cinema_locNum) {
		this.cinema_locNum = cinema_locNum;
	}
	
	
	
	

}